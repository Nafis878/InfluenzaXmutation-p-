def roc_from_auc(auc, n=300, seed=0):
    """Parametric ROC curve from AUC via binormal model (d' = sqrt(2)*Φ⁻¹(AUC))."""
    rng = np.random.RandomState(seed)
    d_prime = np.sqrt(2.0) * ndtri(np.clip(auc, 0.501, 0.999))
    fpr = np.linspace(0, 1, n)
    tpr = ndtr(d_prime - ndtri(np.clip(1 - fpr, 1e-9, 1 - 1e-9)))
    # Add small jitter so lines don't perfectly overlap
    noise = rng.randn(n) * 0.003
    tpr = np.clip(tpr + noise, 0, 1)
    tpr[0] = 0.0; tpr[-1] = 1.0
    return fpr, np.sort(tpr)

